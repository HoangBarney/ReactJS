import React from 'react'
import './Tiramisu.css'
function Tiramisu() {
   return (
   <div className="tiramisu">
      <h3>Ingredients</h3>
     <ul>
        <li>4 egg yolks</li>
        <li>1/2 cup sugar and divided for 2 times</li>
        <li>2 cups heavy cream</li>
        <li>227 grams mascarpone cheese</li>
        
     </ul>
     
     <h3>Instruction:</h3>

     <h4> Step 1: </h4>
     <p> Whip together egg yolks and 1/4 cup sugar until it turn yellow and about tripled in volumn</p>
      
      <h4> Step 2:</h4>
      <p>Whip cream with 1/4 sugar until soft-medium peaks then add cheese and continue to whip until it creates a soft</p>

      <h4> Step 3: </h4>
      <p> Gentlely fold the cheese mixture into the egg yolk until combined</p>

      
   </div>
   )
}
export default Tiramisu