import React from 'react'
import './Roll.css'

function Roll() {
    return (
<div className="roll">
   
<h3>Ingredients</h3>
<ul>

<li>¼ cup warm water</li>
<li>1 package active dry yeast or 2 ¼ teaspoons</li>
<li>¾ cup whole milk</li>
<li>⅓ cup butter</li>
<li>⅓ cup granulated sugar plus 1 teaspoon</li>
<li>½ teaspoon salt</li>
<li>3 ¾ to 4 ¼ cups all purpose flour divided</li>
<li>2 eggs room temperature</li>
<h5>Filling</h5>
<li>½ cup butter softened</li>
<li>1 cup brown sugar packed</li>
<li>2 tablespoons ground cinnamon</li>
<h5>Frosting</h5>
<li>1 ½ cups powdered sugar or as needed</li>
<li> 4 ounces cream cheese softened</li>
<li>¼ cup unsalted butter softened</li>
<li> ½ teaspoon vanilla extract</li>
<li>⅛ teaspoon salt</li>
</ul>
<h3>Instructions</h3>
 <h4>Step 1:</h4> 
 <p>Grease a 9x13 pan or baking dish.</p>
<p>Combine the water, yeast, and 1 teaspoon of sugar in a small bowl. Let the mixture sit for 10 minutes or until it becomes foamy.</p>                        
<h4>Step 2:</h4> 
<p>In a saucepan, combine the milk, butter, remaining sugar, and salt. Heat the mixture to 120-130°F.</p>
<h4>Step 3:</h4> 
<p>Place 2 cups of flour in a stand mixer. Add the eggs, milk mixture, and yeast mixture and mix until combined</p>
<h4>Step 4:</h4> 
<p>Using a dough hook, add the flour ½ cup at a time until the dough forms a soft ball that pulls away from the sides of the bowl. Remove the dough from the bowl and knead it on a lightly floured surface until it is smooth and elastic (about 8 minutes).</p>
<h4>Step 5:</h4> 
<p>Place the dough in a greased bowl in a warm place and cover it with a towel for 1 hour or until it has doubled in size.</p>
<h4>Step 6:</h4> 
<p>Roll the dough out into a 15" x 12" rectangle. Spread butter on the dough and top it with the brown sugar and cinnamon.</p>
<h4>Step 7:</h4> 
<p>Roll the dough up, starting on the long side. Cut it into 15 pieces and place them in the prepared pan.</p>
<h4>Step 8:</h4> 
<p>Cover rolls with a towel and allow them to rise 30-45 minutes. Preheat oven to 375°F.</p>
<p>Brush rolls with milk and bake 20-25 minutes.</p>
<h4>Step 9:</h4>
<p>    While the rolls are baking, combine frosting ingredients with a mixer until fluffy.</p>
<p> Allow rolls to cool for about 10-15 minutes and spread frosting on warm rolls.</p>

</div>
    )
}
export default Roll